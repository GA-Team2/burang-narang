package org.ga2.buna.dto;

public class TrafficDTO {
	private String s_serialnum;
	private String tf_type;
	private String tf_name;
	private String tf_pnumber;
	private String tf_location; 
	private String tf_photo;
	
	
	public String getS_serialnum() {return s_serialnum;}
	public void setS_serialnum(String s_serialnum) {this.s_serialnum = s_serialnum;}
	public String getTf_type() {return tf_type;}
	public void setTf_type(String tf_type) {this.tf_type = tf_type;}
	public String getTf_name() {return tf_name;}
	public void setTf_name(String tf_name) {this.tf_name = tf_name;}
	public String getTf_pnumber() {return tf_pnumber;}
	public void setTf_pnumber(String tf_pnumber) {this.tf_pnumber = tf_pnumber;}
	public String getTf_location() {return tf_location;}
	public void setTf_location(String tf_location) {this.tf_location = tf_location;}
	public String getTf_photo() {return tf_photo;}
	public void setTf_photo(String tf_photo) {this.tf_photo = tf_photo;}
}